#[derive(Debug, Error)]
pub enum AdapterError {
    #[error("{0}")]
    Message(String),
    #[error("{0}")]
    Io(#[from] io::Error),
}

pub fn machine_options_from_config(
    config: &CcyncConfig,
    dry_run: bool,
) -> Result<MachineUpdateOptions, AdapterError> {
    let (repo_root, source_root) = resolve_runtime_roots(config)?;
    let user_home = user_home()?;
    let appdata_root = appdata_root(&user_home);
    let selected_runtimes = load_selected_runtimes(&user_home)?;
    let primary_runtime = load_primary_runtime(&selected_runtimes, &user_home)?;
    Ok(MachineUpdateOptions {
        repo_root,
        source_root,
        user_home,
        appdata_root,
        selected_runtimes,
        primary_runtime,
        dry_run,
        replace: false,
        uninstall: false,
        // D-07: single mode — source projections always run from the canonical
        // root (no Dev-only gate). The managed-set source swap lands in a later task.
        include_source_projections: true,
    })
}

/// Assemble the ordered projection source list for a machine update or sync.
///
/// ccync-core (the canonical plugin root) is always first and persistent.
/// Additional installed sources (bundles, personal layer) are appended
/// when registered — reserved for extension in the bundle-installation layer.
pub fn collect_projection_sources(source_root: &Path) -> Vec<ProjectionSource> {
    vec![ProjectionSource {
        root: source_root.to_path_buf(),
        id: "ccync".to_string(),
        persistent: true,
    }]
}

/// The decomposed components of a single managed plugin directory: the skill and
/// command directory names, the agent file names (`*.agent.md`), and the MCP
/// server names declared in the plugin's `.mcp.json`.
///
/// This is the projection-input materialization — a managed plugin in the cache
/// is decomposed into the four component lists that feed `update_skills` /
/// `update_commands` and the MCP projection. All lists are name-sorted and
/// gracefully empty when the corresponding subtree / manifest is absent.
#[derive(Debug, Clone, Default, PartialEq, Eq)]
pub struct PluginComponents {
    pub skills: Vec<String>,
    pub commands: Vec<String>,
    pub agents: Vec<String>,
    pub mcp_servers: Vec<String>,
}

/// Decompose a managed plugin directory into its component lists.
///
/// `skills` / `commands` are child directory names under `skills/` and
/// `commands/`; `agents` are the `*.agent.md` file names under `agents/`;
/// `mcp_servers` are the keys of the `servers` object in `<plugin>/.mcp.json`.
/// Missing subtrees or manifest degrade to empty lists (no error).
pub fn decompose_plugin(plugin_dir: &Path) -> Result<PluginComponents, AdapterError> {
    let skills = list_named_children(&plugin_dir.join("skills"))?;
    let commands = list_named_children(&plugin_dir.join("commands"))?;
    let agents = list_files_with_extension(&plugin_dir.join("agents"), "agent.md")?
        .into_iter()
        .filter_map(|p| p.file_name().and_then(OsStr::to_str).map(str::to_string))
        .collect();
    let mut mcp_servers: Vec<String> = Vec::new();
    if let Ok(content) = fs::read_to_string(plugin_dir.join(".mcp.json")) {
        if let Ok(v) = serde_json::from_str::<Value>(&content) {
            if let Some(servers) = v.get("servers").and_then(Value::as_object) {
                mcp_servers = servers.keys().cloned().collect();
            }
        }
    }
    mcp_servers.sort();
    Ok(PluginComponents {
        skills,
        commands,
        agents,
        mcp_servers,
    })
}

pub fn run_machine_update(opts: &MachineUpdateOptions) -> Result<ProjectionReport, AdapterError> {
    let mut report = run_update_personalization(&PersonalizationUpdateOptions {
        repo_root: opts.repo_root.clone(),
        source_root: opts.source_root.clone(),
        user_home: opts.user_home.clone(),
        appdata_root: opts.appdata_root.clone(),
        selected_runtimes: opts.selected_runtimes.clone(),
        dry_run: opts.dry_run,
        replace: opts.replace,
        uninstall: opts.uninstall,
    })?;

    if opts.include_source_projections {
        let sources = collect_projection_sources(&opts.source_root);
        extend_report(
            &mut report,
            run_update_skills(&SkillUpdateOptions {
                repo_root: opts.repo_root.clone(),
                sources: sources.clone(),
                user_home: opts.user_home.clone(),
                selected_runtimes: opts.selected_runtimes.clone(),
                dry_run: opts.dry_run,
                replace: opts.replace,
            })?,
        );
        extend_report(
            &mut report,
            run_update_commands(&CommandUpdateOptions {
                repo_root: opts.repo_root.clone(),
                sources,
                user_home: opts.user_home.clone(),
                selected_runtimes: opts.selected_runtimes.clone(),
                dry_run: opts.dry_run,
                replace: opts.replace,
            })?,
        );
    }

    Ok(report)
}

fn extend_report(report: &mut ProjectionReport, mut next: ProjectionReport) {
    report.written_files.append(&mut next.written_files);
    report.removed_paths.append(&mut next.removed_paths);
    report.created_links.append(&mut next.created_links);
    report.warnings.append(&mut next.warnings);
}

/// Resolve the projection roots. D-07: single mode — `repo_root` and
/// `source_root` are both the ccync canonical plugin root (`~/.ccync/plugins/ccync`),
/// which the canonical-root content render materializes. There is no Dev/Normal split and
/// no ccync-core lookup, so this never errors for "missing ccync-core" — only when the
/// home directory cannot be resolved.
fn resolve_runtime_roots(_config: &CcyncConfig) -> Result<(PathBuf, PathBuf), AdapterError> {
    let canonical = ccync_foundation::paths::canonical_plugin_root()
        .ok_or_else(|| AdapterError::Message("could not resolve canonical plugin root".into()))?;
    Ok((canonical.clone(), canonical))
}

fn user_home() -> Result<PathBuf, AdapterError> {
    #[cfg(windows)]
    {
        std::env::var_os("USERPROFILE")
            .map(PathBuf::from)
            .ok_or_else(|| AdapterError::Message("USERPROFILE is not set".into()))
    }
    #[cfg(not(windows))]
    {
        std::env::var_os("HOME")
            .map(PathBuf::from)
            .ok_or_else(|| AdapterError::Message("HOME is not set".into()))
    }
}

fn appdata_root(user_home: &Path) -> PathBuf {
    #[cfg(windows)]
    {
        std::env::var_os("APPDATA")
            .map(PathBuf::from)
            .unwrap_or_else(|| user_home.join("AppData").join("Roaming"))
    }
    #[cfg(not(windows))]
    {
        user_home.join(".config")
    }
}

fn load_selected_runtimes(user_home: &Path) -> Result<Vec<String>, AdapterError> {
    let install_state = user_home.join(".ccync").join("install-state.json");
    let raw = read_json_map(&install_state)?;
    let mut selected = raw
        .get("selectedRuntimes")
        .and_then(Value::as_array)
        .map(|values| {
            values
                .iter()
                .filter_map(Value::as_str)
                .map(|value| value.trim().to_ascii_lowercase())
                .filter(|value| VALID_RUNTIMES.contains(&value.as_str()))
                .collect::<Vec<_>>()
        })
        .unwrap_or_default();
    if selected.is_empty() {
        selected = VALID_RUNTIMES
            .iter()
            .map(|value| (*value).to_string())
            .collect();
    }
    selected.sort();
    selected.dedup();
    Ok(selected)
}

fn load_primary_runtime(
    selected_runtimes: &[String],
    user_home: &Path,
) -> Result<String, AdapterError> {
    let install_state = user_home.join(".ccync").join("install-state.json");
    let raw = read_json_map(&install_state)?;
    if let Some(primary) = raw.get("primaryRuntime").and_then(Value::as_str) {
        let normalized = primary.trim().to_ascii_lowercase();
        if selected_runtimes.iter().any(|value| value == &normalized) {
            return Ok(normalized);
        }
    }
    let refs = selected_runtimes
        .iter()
        .map(String::as_str)
        .collect::<Vec<_>>();
    default_primary_runtime(&refs)
        .map(str::to_string)
        .ok_or_else(|| AdapterError::Message("could not resolve primary runtime".into()))
}

